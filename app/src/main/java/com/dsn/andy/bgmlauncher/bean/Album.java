    public static ArrayList<Music> createAlbum2(String[] infos) {
        ArrayList<Music> musics = new ArrayList<Music>();

        for (int i = 0; i < infos.length; i += 2) {
            Music m = new Music();
            m.singer = infos[i];
            m.name = infos[i + 1];
            m.position=i/2;
            musics.add(m);
        }

        return musics;
    }

